<?php
// Define the numbers
$num1 = 5.99;
$num2 = 10.55;

// Using ceil() function (rounds up)
$ceil1 = ceil($num1);
$ceil2 = ceil($num2);

// Using sqrt() function (square root)
$sqrt1 = sqrt($num1);
$sqrt2 = sqrt($num2);

// Display results
echo "Number 1: $num1\n";
echo "Number 2: $num2\n\n";

echo "CEIL FUNCTION:\n";
echo "ceil($num1) = $ceil1\n";
echo "ceil($num2) = $ceil2\n\n";

echo "SQRT FUNCTION:\n";
echo "sqrt($num1) = $sqrt1\n";
echo "sqrt($num2) = $sqrt2\n";
